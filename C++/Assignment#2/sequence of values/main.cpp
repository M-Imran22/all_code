#include <iostream>

using namespace std;

int main()
{
    cout <<"The following sequence of values \n";
    for (int i=2; i<=12; i+=2)
        cout <<i<<",";
        cout <<"\n";
    for (int j=3; j<=23; j+=5)
        cout <<j<<" ";
        cout <<"\n";
    for (int k=-4; k<=2; k++)
        cout <<k<<" ";
        cout <<"\n";
    for (int a=19; a<=51; a+=8)
        cout <<a<<" ";
        cout <<"\n";
    return 0;
}
